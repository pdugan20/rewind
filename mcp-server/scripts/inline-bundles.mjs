#!/usr/bin/env node
/**
 * Build each HTML entry in web/ via Vite's programmatic API and emit
 * src/ui-bundles.ts with one exported constant per HTML file.
 *
 * Why programmatic + Promise.all over per-entry `vite build` CLI calls:
 * each `vite build` cold start is ~5-7s of Node + plugin loader overhead.
 * Paid once and amortized across all 11 entries it's ~0.5s total; paid
 * 11x via execSync it's ~70-90s. Same byte-for-byte output, no plugin
 * surgery needed — vite-plugin-singlefile is single-input by design but
 * "single Node process running 11 builds" satisfies that constraint.
 *
 * Called from `build:web`. Takes no arguments — entries are discovered
 * from web/*.html.
 */
import { build } from 'vite';
import react from '@vitejs/plugin-react';
import { viteSingleFile } from 'vite-plugin-singlefile';
import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const webDir = join(root, 'web');
const distDir = join(webDir, 'dist');
const outFile = join(root, 'src', 'ui-bundles.ts');

const entries = readdirSync(webDir).filter(
  (f) => f.endsWith('.html') && f !== 'dist'
);
if (!entries.length) {
  console.error(`No .html entries found in ${webDir}`);
  process.exit(1);
}

const t0 = Date.now();

// Inject the loading-surface body bg directly into each HTML's <head>
// so the browser paints it as soon as the iframe document loads —
// before our JS bundle parses and before card-tokens.ts runs its
// runtime CSS injection. Without this, iOS WebKit briefly shows
// browser-default white in the gap between Claude's shimmer
// disappearing and our React app mounting. Values match Claude's
// known loading-surface colors (#F3F0EF light / #121212 dark) so
// the transition into our card chrome is seamless.
const LOADING_BG_STYLE = `<style>
html, body {
  margin: 0;
  padding: 0;
  background: #F3F0EF;
}
@media (prefers-color-scheme: dark) {
  html, body {
    background: #121212;
  }
}
</style>`;
const injectLoadingBg = {
  name: 'rewind-inject-loading-bg',
  // Run after vite-plugin-singlefile has inlined the bundle into
  // <head>. We want this style to be the FIRST element in <head>
  // so the browser applies the body bg before parsing the giant
  // inline <script>. Putting it at the end of <head> meant the
  // 450KB inline script had to be parsed first, producing a brief
  // white flash on iOS before body bg painted.
  enforce: 'post',
  transformIndexHtml(html) {
    return html.replace(/<head>\s*/, `<head>\n${LOADING_BG_STYLE}\n`);
  },
};

// allSettled so a syntax error in one component doesn't mask which other
// entries succeeded. We surface the failure list explicitly afterward.
const results = await Promise.allSettled(
  entries.map((entry) =>
    build({
      root: webDir,
      plugins: [react(), injectLoadingBg, viteSingleFile()],
      logLevel: 'warn',
      build: {
        outDir: distDir,
        emptyOutDir: false, // each build writes only its own entry
        rollupOptions: { input: join(webDir, entry) },
      },
    })
  )
);

const failures = results
  .map((r, i) =>
    r.status === 'rejected' ? { entry: entries[i], reason: r.reason } : null
  )
  .filter(Boolean);
if (failures.length) {
  for (const f of failures) {
    console.error(`\n[build:web] FAILED ${f.entry}:`);
    console.error(f.reason?.stack ?? f.reason);
  }
  process.exit(1);
}

function toConstName(filename) {
  const base = filename.replace(/\.html$/, '');
  return (
    base
      .toUpperCase()
      .replace(/[^A-Z0-9]+/g, '_')
      .replace(/^_|_$/g, '') + '_HTML'
  );
}

const built = readdirSync(distDir).filter((f) => f.endsWith('.html'));
const parts = [
  '// Auto-generated by scripts/inline-bundles.mjs -- do not edit.',
];
const manifest = [];
for (const f of built) {
  const html = readFileSync(join(distDir, f), 'utf8');
  const name = toConstName(f);
  parts.push(`export const ${name} = ${JSON.stringify(html)};`);
  manifest.push({ file: f, name, size: html.length });
}

parts.push('');
parts.push('export const UI_BUNDLES: Record<string, string> = {');
for (const { file, name } of manifest) {
  parts.push(`  ${JSON.stringify(file)}: ${name},`);
}
parts.push('};');
parts.push('');

writeFileSync(outFile, parts.join('\n'));

const elapsed = ((Date.now() - t0) / 1000).toFixed(2);
console.log(`\nBuilt ${entries.length} entries in ${elapsed}s.`);
console.log(`Wrote ${outFile}:`);
for (const { file, size } of manifest) {
  console.log(`  - ${file}: ${size.toLocaleString()} chars`);
}
